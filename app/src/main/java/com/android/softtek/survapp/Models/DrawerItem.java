package com.android.softtek.survapp.Models;

public class DrawerItem {
    private String title;
    private int icon;

    public DrawerItem() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }
}
